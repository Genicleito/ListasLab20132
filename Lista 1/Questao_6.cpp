#include <stdio.h>
#include <stdlib.h>

int main() {
	int num;
	
	if (num=10) {
		printf("Testando o programa!");
	}
	
	return 0;
}

/*Na condição if (que significa SE), a variavel num recebeu um determinado valor (num=10),
desse modo, como isso é verdade, o programa executará os comandos que estão dentro dessa condição!*/
